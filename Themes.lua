-- Function to apply theme
function Themes.Apply(SpectrumX, themeName)
    local theme = Themes[themeName]
    if theme then
        for key, value in pairs(theme) do
            SpectrumX.Theme[key] = value
        end
        return true
    end
    return false
end

-- Function to get theme names
function Themes.GetNames()
    local names = {}
    for name, _ in pairs(Themes) do
        if name ~= "Apply" and name ~= "GetNames" then
            table.insert(names, name)
        end
    end
    return names
end

return Themes
